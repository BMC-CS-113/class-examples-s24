public class Point {
    //private member variables
    private int x;
    private int y;

    //Let's create a constructor
    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    //Let's create getters 
    private int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
    
}
